        //
        //POST :/Account/InviteSignUp/
        [HttpPost]
        [AllowAnonymous]
        public async Task<IActionResult> InviteSignUp(SubscriberRegisterViewModel model)
        {
            if (ModelState.IsValid)
            {
                InviteCodeResult codeResult = await _userService.GetCodeDetails(model.Code);

                if (codeResult.Success)
                {
                    var user = new SubscriberUser { UserName = model.Email, Email = model.Email, Name = model.Name };
                    var result = await _userManager.CreateAsync(user, model.Password);

                    if (result.Succeeded)
                    {
                        await _userManager.AddToRoleAsync(user, "Subscriber");

                        await SendEmailConfirmation(user);

                        await _userService.AddUserToPractice(user.Id, codeResult.PracticeId);
                        return RedirectToAction("Index", "SubscriberDashboard");

                    }
                    AddErrors(result);
                }
                ModelState.AddModelError("", codeResult.Error);
            }
            return View(model);
        }

        //
        // GET: /Account/Register
        [HttpGet]
        [AllowAnonymous]
        [Route("Account/Register/{practiceSlug?}/{marketingSlug?}")]
        public async Task<IActionResult> Register(string practiceSlug = null, string marketingSlug = null)
        {
            var rvm = new RegisterViewModel();

            if (!string.IsNullOrEmpty(practiceSlug))
            {
                rvm.PracticeSlug = practiceSlug;

                Practice practice = await _practiceService.GetPracticeBySignupSlug(practiceSlug);

                if (practice == null)
                {
                    rvm.WithPractice = true;
                    rvm.Error = true;
                } else
                {
                    rvm.WithPractice = true;
                    rvm.PracticeId = practice.Id;
                    rvm.PracticeName = practice.Name;
                    rvm.Error = false;
                }              
            } else
            {
                rvm.WithPractice = false;
            }

            if (!string.IsNullOrEmpty(marketingSlug))
            {
                rvm.MarketingCode = marketingSlug;
                await _clientRelationsService.SetMarketingCodeAsOpened(marketingSlug);
            }

            return View(rvm);
        }

        //
        // POST: /Account/Register
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Register(RegisterViewModel model)
        {
            if (ModelState.IsValid)
            {
                var user = new ClientUser { UserName = model.Email, Email = model.Email, Name = model.Name };
                var result = await _userManager.CreateAsync(user, model.Password);
                if (result.Succeeded)
                {
                    await _userManager.AddToRoleAsync(user, "Client");
                    await SendEmailConfirmation(user);
                    
                    if (model.WithPractice)
                    {
                        await _placesService.AddSharePlaceAtRegistration(user.Id, model.PracticeId);
                    }
                    
                    if (!string.IsNullOrEmpty(model.MarketingCode))
                    {
                        await _clientRelationsService.SetMarketingCodeAsRegistered(model.MarketingCode);
                    }           

                    _logger.LogInformation(3, "User created a new account with password.");
                    return RedirectToAction(nameof(RegisterSuccess));
                }
                AddErrors(result);
            }

            // If we got this far, something failed, redisplay form
            return View(model);
        }

